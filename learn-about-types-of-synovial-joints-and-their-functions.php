                        <h2>Types of Synovial Joints
                        </h2>
                        <p>Types of synovial joints are based on their mobility. Mainly there are six types of synovial joints, that are described in brief below.
                        </p>
                        <h3>1. Plain Joints
                        </h3>
                        <p>Plain Joints are also said to be gliding joints. These joints have bones with articulating surfaces and are found in the tarsal bones of the foot, carpal bones of the hands, and also in between vertebrae. An example of such a joint type is the wrist carpals. Also, the acromioclavicular joint is yet another example of such joint type. These joints facilitate the sliding and gliding manoeuvres of the body. They also facilitate the articulation between the vertebrae.
                        </p>
                        <h3>2. Pivot joints
                        </h3>
                        <p>Pivot Joints of the body allow rotational joint movements. These joints consist of the rounded end of one bone fitting into a ring formed by the other bone. We can rotate our neck or head just because of pivot joints in the first & second vertebrae of the head & neck.
                        </p>
                        <p>Examples of pivot joints include the proximal-radioulnar joint, atlantoaxial joint, and distal-radioulnar joint. The pivot joints are such types of joints in the human body that facilitate the rotation of one bone with the help of other bones in the body.
                        </p>
                        <h3>3.Condyloid joints
                        </h3>
                        <p>Condyloid Joints are also said to be Ellipsoidal joints of the body. These joints consist of an oval-shaped end of one bone fitting or adjacent to the similar oval-shaped hollow of another bone. Angular movements of two bones can be done through condyloid joints.
                        </p>
                        <p>The <a href="https://radiopaedia.org/articles/condyloid-joint">condyloid joint</a> is a kind of ball & socket joint. Such types of Synovial joints allow primary manoeuvres within axes that are perpendicular to each other. With the ellipsoidal joints, secondary or passive movements can occur on a separate axis. Overall, it can be said that the ellipsoidal joints or the condyloid joints allow extension and flexion manoeuvres within the body.
                        </p>
                        <h4>4.Hinge joints
                        </h4>
                        <p>Hinge Joints are those which help in fitting the rounded end of one bone into the hollow end of another bone in the human body. With the help of hinge joints, one bone can easily move while the other remains constant.
                        </p>
                        <p>In the human body, the hinge joints are found in the elbow and between the ulna and the humerus. These joints act similar to that of a door hinge. These joints also allow extension and flexion in just a single plane.
                        </p>
                        <h3>5.Ball & socket joints
                        </h3>
                        <p>Ball & socket joint is also referred to as Universal joints. These bones help a rounded shape end of a bone fitting into a cuplike socket of another bone.
