                        </li>
                        <li>2. Lift hips so that they are in line with shoulders, knees bent, feet on the floor, or elevated (e.g., foot of bed).
                        </li>
                        <li>3. Stack the right leg on top of the left so the body forms a straight line from ankles to shoulders.
                        </li>
